export async function seedAccessControlV2() {
  for (const code of permissionCodes) {
    await prisma.permission.upsert({
      where: { code },
      update: {
        description: `Seeded by access-control-v2 bootstrap`,
      },
      create: {
        uid: code,
        code,
        description: `Seeded by access-control-v2 bootstrap`,
      },
    });
  }

  for (const policy of policies) {
    const createdPolicy = await prisma.policy.upsert({
      where: { code: policy.code },
      update: {
        name: policy.name,
        description: policy.description,
        role: policy.role,
        group: policy.group,
        isSystem: true,
      },
      create: {
        code: policy.code,
        name: policy.name,
        description: policy.description,
        role: policy.role,
        group: policy.group,
        isSystem: true,
      },
    });

    for (const permissionCode of policy.permissions) {
      const permission = await prisma.permission.findUnique({ where: { code: permissionCode } });
      if (!permission) continue;

      await prisma.policyPermission.upsert({
        where: {
          policyUid_permissionUid: {
            policyUid: createdPolicy.uid,
            permissionUid: permission.uid,
          },
        },
        update: {},
        create: {
          policyUid: createdPolicy.uid,
          permissionUid: permission.uid,
        },
      });
    }
  }
}
